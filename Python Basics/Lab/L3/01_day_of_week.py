day_number = int(input())
reply = ""
if day_number == 1:
    reply = "Monday"

elif day_number == 2:
    reply = "Tuesday"

elif day_number == 3:
    reply = "Wednesday"

elif day_number == 4:
    reply = "Thursday"

elif day_number == 5:
    reply = "Friday"

elif day_number == 6:
    reply = "Saturday"

elif day_number == 7:
    reply = "Sunday"

else:
    reply = "Error"

print(reply)
